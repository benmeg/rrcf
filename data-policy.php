<h1>Data policy</h1>
<h2>Storing your information</h2>
<p>Cardiff University is the Data Controller and is committed to respecting and protecting your personal data in accordance with your expectations and Data Protection legislation. The University has a Data Protection Officer who can be contacted at <a href="mailto:inforequest@cardiff.ac.uk">inforequest@cardiff.ac.uk</a>. Further information about Data Protection, including your rights and details about how to contact the Information Commissioner's Office should you wish to complain, can be found at the following:<br />
<br />
<a href="https://www.cardiff.ac.uk/public-information/policies-and-procedures/data-protection" target="_blank">https://www.cardiff.ac.uk/public-information/policies-and-procedures/data-protection</a>
</p>

<p>All data (both personal and generated) will be stored on a secure server at Cardiff University; All data will be securely stored and will be encrypted where possible. User generated data (e.g. answers to questions) may be shared with our research collaborators. No personally identifiable information will be shared beyond the research team (e.g. email address, ethnicity, gender).</p>

<p>Access to the server is restricted to members of the research team, and only via secure channels.</p>

<p>Personal data will be processed in accordance with the General Data Protection Regulation (GDPR) as per the privacy notice below.</p>

<h2>Privacy notice</h2>
<p>The information provided will be held in compliance with GDPR requirements.</p>

<p>Cardiff University is the Data Controller and is committed to respecting and protecting your personal data in accordance with your expectations and Data Protection legislation. The University has a Data Protection Officer who can be contacted at <a href="mailto:inforequest@cardiff.ac.uk">inforequest@cardiff.ac.uk</a>. Further information about Data Protection, including your rights and details about how to contact the Information Commissioner's Office should you wish to complain, can be found at the following:<br />
<br />
<a href="https://www.cardiff.ac.uk/public-information/policies-and-procedures/data-protection" target="_blank">https://www.cardiff.ac.uk/public-information/policies-and-procedures/data-protection</a>

<p>The lawful basis for the processing of the data you provide is public interest.</p>

<p>The information you provide will be used for two purposes: 1) research and 2) for displaying aggregate metrics on the website dashboard.</p>

<p>Within 15 years of the end of the project, the identifying data (i.e. profile information and optional feedback comments) will be deleted. Anonymised data will be retained indefinitely (under a Creative Commons <a title="CC BY licence explanation" href="https://creativecommons.org/licenses/by/4.0/" target="_blank">CC BY 4.0</a> licence) and stored publicly.</p>
 
<h2>Sharing your information</h2>
<p>The results of the study will be presented at medical and scientific conferences and published in scientific journals, blogs, and the media. A subset of aggregate rating data will also be presented on the website's dashboard in real time. In both cases, the data will be anonymised and will not include any information that will allow you to be personally identified. Cardiff University will not share your personally identifiable information outside of the research team without your explicit consent or where required to do so by law. Data analysis and interpretation is attributable to the researcher rather than you as a research participant.</p>
 
<h2>Withdrawing your data</h2>
<p>Your personally identifiable information and any optional comments you provide as part of this study can be withdrawn at any time. All rating data is contributed under a Creative Commons <a title="CC BY licence explanation" href="https://creativecommons.org/licenses/by/4.0/" target="_blank">CC BY 4.0</a> licence and therefore cannot be withdrawn. We will retain personally identifiable information for a maximum of 15 years after project completion.</p>

