<h2>Resources for Journal Editors</h2>

<p>If you're a journal editor and wish to collect anonymous feedback on the Registered Reports process at your journal, you can refer authors and reviewers to this website.<br /><br />
There are three main ways to do this:</p>
<ol>
	<li>
		Email previous Registered Reports authors and reviewers at your journal, providing them with a link to this website (<a href="https://docs.google.com/document/d/1RZrvN-MPXJYNe1G62YTjj410EC-oA7_Jw7_J4iA17MU/view" target="_blank" title="Example email text to send to previous authors/reviewers of Registered Reports at your journal" href="">example text</a>)	
	</li>
	<li>
		Include a link to this website in the final decision letters (Stage 1 and 2) sent to authors and reviewers (<a href="https://docs.google.com/document/d/1YZ2pOMOFII6gpyX9N1wsBdQNAoVxOJGSbwIMgBPJlp8/view" target="_blank" title="Example final decision letter text to send to authors/reviewers at Stage 1 and 2">example text</a>)	
	</li>
	<li>
		Add a link to the Registered Reports section of your journal's website (<a href="https://docs.google.com/document/d/1d0US_wpb_p82Hnq6GXgeM3lS3DWoRoqMSCnMgVL7ACY/view" target="_blank" title="Example link text for Registered Report section of your journal's website">example text</a>)
	</li>
</ol>
<p>
We would encourage you to implement all three of the above - especially 1 and 2.<br /><br />
You are welcome to implement these without consulting us, but we'd love to hear from you if you've implemented them.<br />
<br />Likewise, if you have questions, please <a href="/feedback/contact/">contact us</a>.
</p>
