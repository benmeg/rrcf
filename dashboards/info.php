<h1>How are these ratings calculated?</h1>

<h2>General info</h2>
<p>Ratings are averages of 1-5 scores across responses. Averages for stage/role are calculated and combined using weighted averages to form supra-level categories.</p>
<p>For instance in the first 'By stage' table, <i>Stage 1: Speed</i> is a weighted average of the following averages from the 'By role x Stage' table: <i>Author (Stage 1): Speed</i> and <i>Reviewer (Stage 1): Speed</i>.</p>
<h3>Speed category</h3>
<p>This is a simple average of 1-5 star ratings, weighted by the number of responses for a given stage/role.</p>
<h3>Quality category</h3>
<p>This rating also includes certain non-star rating questions/responses, standardised to 1-5 ratings and combined with any 1-5 star ratings as a weighted average.</p>

<h2>No data yet vs. Not enough data</h2>
<p>Average ratings for a category or question item are shown if there are 5 or more responses.</p>
<h3>No data yet</h3>
<p>This is shown if there are no responses for a category or question item.</p>

<h3>Not enough data</h3>
<p>This is shown if there are between 1-4 responses for a category or question item.</p>

