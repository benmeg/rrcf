<h1>How are these ratings calculated?</h1>

<p>Ratings are averages of 1-5 scores across feedback responses, combined across author and reviewer feedback.</p>

<h2>No data yet vs. Not enough data</h2>
<p>Average ratings for a journal are shown if 5 or more pieces of feedback (and at least 5 response ratings) have been completed by authors or reviewers.</p>
<h3>No data yet</h3>
<p>This is shown if no pieces of feedback have been completed for a journal.</p>

<h3>Not enough data</h3>
<p>This is shown if between 1-4 pieces of feedback have been completed for a journal.</p>

